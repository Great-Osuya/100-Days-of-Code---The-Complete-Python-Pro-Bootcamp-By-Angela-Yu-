#Comparator Operators
# '>' Greater than
# '<' Less than
# '>=' Greater than or equal to
# '<= Less than or equal to
# '==' Equal to
# '!=' Not equal to

# The main difference between '=' and '==' is this:
# '=' means you're assigning the value on the right side to the variable on the left side.
# While '==' serves as a comparison tool to determine if two values are the same.
# '=' is used for variable assignment, while '==' is used to check equality.